package FinalProject;

public class Extra
{
	private String asurite;
	private int minutes;
	
	public Extra(String asurite, int minutes)
	{
		this.asurite = asurite;
		this.minutes = minutes;
	}
	
	public int getMinutes()
	{
		return minutes;
	}
	public String getAsurite()
	{
		return asurite;
	}
}